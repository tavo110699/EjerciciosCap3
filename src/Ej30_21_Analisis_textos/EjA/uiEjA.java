    public void letters(String texto) {
        modelo = new DefaultTableModel(datos1, info);
        tblLetters.setModel(modelo);
        
        char[] letras = new char[texto.length()];
        String[] strLetras = new String[texto.length()];

        letras = texto.toCharArray();
        for (int i = 0; i < letras.length; i++) {
            strLetras[i] = String.valueOf(letras[i]);
        }

        for (int i = 0; i < strLetras.length; i++) {
            for (int j = 0; j < strLetras.length - 1; j++) {
                if (i != j) {
//En este parte sustituimos el "==" por el "equals"
                    if (strLetras[i].equals(strLetras[j])) {
// eliminamos su valor
                        strLetras[i] = "";
                    }
                }
            }
        }

        // mostramos unicamente los que tienen valor
        int n = strLetras.length;
 Arrays.sort(strLetras); 
        for (int k = 0; k <= n - 1; k++) {

            if (strLetras[k] != "") {
               
         //       System.out.println("+-       " + strLetras[k] + "       -+-       " + numeroDeVeces + "       -+");
                numeroDeVeces = contarCaracteres(texto, strLetras[k]);
                 modelo.addRow(new Object[]{strLetras[k], numeroDeVeces});

                //        System.out.println("El caracter " + strLetras[k] + " aparece " + numeroDeVeces + " veces");
            }

        }

    }

    public static int contarCaracteres(String cadena, String caracter) {
        int posicion, contador = 0;
        //se busca la primera vez que aparece
        posicion = cadena.indexOf(caracter);
        while (posicion != -1) { //mientras se encuentre el caracter
            contador++;           //se cuenta
            //se sigue buscando a partir de la posiciÃ³n siguiente a la encontrada
            posicion = cadena.indexOf(caracter, posicion + 1);
        }
        return contador;
    }
    
      public void clean() {
        int a = modelo.getRowCount() - 1;
        
        for (int i = a; i >= 0; i--) {
            modelo.removeRow(modelo.getRowCount() - 1);
        }
        txtText.setText("");
    }
    
    
